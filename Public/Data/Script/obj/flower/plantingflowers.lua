end

function x000175_GetSceneIdByPlayerLevel(playerLevel)
	if playerLevel >= 100 then -- 大于等于100级的玩家则所有的场景都是随机的
		local index = random(getn(x000175_g_scenePosInfoList))
		return x000175_g_scenePosInfoList[index].sceneId
	else
	local j = 1
	local sceneTable = {}
	for i, v in x000175_g_scenePosInfoList do
		if playerLevel >= v.minL and playerLevel <= v.maxL then
			sceneTable[j] = v.sceneId
			j = j + 1
		end
	end
	local index = random(getn(sceneTable))
	return sceneTable[index]
end
end

function x000175_GetScenePosInfo(sceneId,selectSceneId)

	local index = random(getn(x000175_g_scenePosInfoList))
	local subIndex = random(getn(x000175_g_scenePosInfoList[index].posList))
		
	local sceneId = x000175_g_scenePosInfoList[index].sceneId
	local sceneName = x000175_g_scenePosInfoList[index].sceneName
	local x		 	= x000175_g_scenePosInfoList[index].posList[subIndex].x
	local z 		= x000175_g_scenePosInfoList[index].posList[subIndex].z
	local r 		= x000175_g_scenePosInfoList[index].posList[subIndex].r
	
	if selectSceneId >=0 and selectSceneId ~= -1 then
		for i, v in x000175_g_scenePosInfoList do
			if v.sceneId == selectSceneId then
				local index = random(getn(v.posList))
				--PrintStr("..." .. v.sceneName)
				sceneId, sceneName, x, z, r = v.sceneId, v.sceneName, v.posList[index].x, v.posList[index].z, v.posList[index].r
			end
		end
	end
	
	if nil==sceneId or nil==sceneName
	 or nil==x or nil==z or nil==r then
		local logstr = "GetScenePosInfo..selectSceneId=" .. selectSceneId .. "impossible!!!"
		MissionLog(sceneId, logstr);
	end	

	return sceneId, sceneName, x, z, r	
		
end

