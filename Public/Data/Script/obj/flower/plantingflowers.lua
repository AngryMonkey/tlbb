function x000175_CreateMonsterbyHumanLevel(sceneId, selfId, x, z)
	local humanLevel = LuaFnGetLevel(sceneId, selfId)
	local dataId = x000175_g_MonsterConfigTable[1].id
	local ct = getn(x000175_g_MonsterConfigTable)
	for i=1, ct do
		if humanLevel >= x000175_g_MonsterConfigTable[i].level then
			if i+1 <= ct then
				if humanLevel < x000175_g_MonsterConfigTable[i+1].level then
					dataId = x000175_g_MonsterConfigTable[i].id
					break
				end	
			end	
		end
	end
	
	local aifile = random(10)
	local MonsterId = LuaFnCreateMonster(sceneId, dataId, x, z, 0, aifile, -1)
	SetLevel(sceneId, MonsterId, humanLevel+(random(2)-random(2)) )
	SetCharacterDieTime(sceneId, MonsterId, 20*60000)
	
	local strTitle, strName = x000175_CreateTitleAndName(sceneId,selfId)
	SetCharacterTitle(sceneId, MonsterId, strTitle)
	SetCharacterName(sceneId, MonsterId, strName)			
	
end
			
function x000175_ProduceItemParamData(sceneId, selfId, BagPos)
	local index = random(getn(x000175_g_scenePosInfoList))
	local sId = x000175_g_scenePosInfoList[index].sceneId
	local scene, sceneName, x, z, r = x000175_GetScenePosInfo(sceneId,sId)

	if nil == BagPos or BagPos < 0 then
		local logstr = "chengxiongdatu::x000175_ProduceItemParamData.. BagPos=" .. BagPos 
		MissionLog(sceneId, logstr)
		return
	end
	if scene == nil or sceneName == nil 
		or x == nil or z == nil or r == nil then
		local logstr = "chengxiongdatu::x000175_ProduceItemParamData.."
			.. "scene=" .. type(scene) .. ",sceneName=" .. type(sceneName)
			.. "x=" .. type(x) .. ",z=" .. type(z) .. ",r=" .. type(r)
		
		MissionLog(sceneId, logstr)
		return
	end
	
	local LogInfo	= format( "[ChengXiong]: x000175_ProduceItemParamData( sceneId=%d, GUID=%0X ), Scene=%d, SceneName=%s, MissionX=%d, MissionZ=%d, MissionR=%d",
														sceneId,
														LuaFnObjId2Guid( sceneId, selfId ),
														scene,
														sceneName,
														x,
														z,
														r)	
	MissionLog(sceneId, LogInfo)	
	
	SetBagItemParam(sceneId, selfId, BagPos, 0, 0, 1)
	SetBagItemParam(sceneId, selfId, BagPos, 1, 1, scene)
	SetBagItemParam(sceneId, selfId, BagPos, 3, 1, x)
	SetBagItemParam(sceneId, selfId, BagPos, 5, 1, z)
	SetBagItemParam(sceneId, selfId, BagPos, 7, 1, r)

end
