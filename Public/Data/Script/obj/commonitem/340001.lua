--**********************************
function x340001_OnDeplete( sceneId, selfId )

  local Level = GetLevel( sceneId, selfId )
  local MenPai = LuaFnGetMenPai( sceneId, selfId )
  
  if Level < 10 then
      return 0
  end
  
  if MenPai < 0 or MenPai >8 then           
 		  BeginEvent(sceneId)
			  strText = format("没有加入门派，不能使用此物品")
			  AddText(sceneId,strText)
 		  EndEvent(sceneId)

 		  DispatchMissionTips(sceneId,selfId)
 		  
      return 0
  end
  
  if IsHaveMission(sceneId,selfId,4021) > 0 then
		BeginEvent(sceneId)
			strText = "您处于漕运状态中，不可以使用传送功能。"
			AddText(sceneId,strText);
		EndEvent(sceneId)
		DispatchMissionTips(sceneId,selfId)
		return 0
	end
  
  	-- 检测玩家身上是不是有“银票”这个东西，有就不能使用这里的功能
	if GetItemCount(sceneId, selfId, 40002000)>=1  then
		BeginEvent( sceneId )
			AddText( sceneId, "你身上有银票，正在跑商！不可以使用传送功能。" )
		EndEvent( sceneId )
		DispatchMissionTips( sceneId, selfId )
		return 0
	end
  
  --检测Impact状态驻留效果
	for i, ImpactId in x340001_g_Impact_NotTransportList do
		if LuaFnHaveImpactOfSpecificDataIndex(sceneId, selfId, ImpactId) ~= 0 then
			BeginEvent(sceneId)			
				AddText(sceneId, x340001_g_TalkInfo_NotTransportList[i]);
			EndEvent(sceneId)
			DispatchMissionTips(sceneId,selfId)
			return 0
		end
	end
  
	if(0<LuaFnDepletingUsedItem(sceneId, selfId)) then
		return 1;
	end
	return 0;
end



