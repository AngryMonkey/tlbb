            return 1
        end
    end

    return 0    
end

function x050016_GetValidIndex( PosList, IndexList )
    local PosListSize = getn( PosList )
    local IndexListSize = getn( IndexList )
    
    local i = 0
    local Data = 1
    while i<50 do
        Data = random( PosListSize )
        local bInList = x050016_InList( Data, IndexList )
        if  0 == bInList  then
            i = 50
        end
        i = i + 1      --为防止死循环,当 i超过50时,将退出循环
    end
    
    IndexList[ IndexListSize + 1 ] = Data
    
end


function x050016_CollectIndex( PosList, IndexList, IndexListSize )
    for i=1, IndexListSize do
        x050016_GetValidIndex( PosList, IndexList )
    end
    
end

