	local npcobjid
	if param5~=nil and param5 == 1000 then
		for i = 1,20 do 
			npcobjid = LuaFnCreateMonster( sceneId, 11, x801016_g_Examinant_Position[i].x,x801016_g_Examinant_Position[i].z, 3, -1, 801017 )
