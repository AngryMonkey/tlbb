function x229020_CreateMonsterbyHumanLevel(sceneId, selfId, x, z)
	local humanLevel = LuaFnGetLevel(sceneId, selfId)
	local dataId = x229020_g_MonsterConfigTable[1].id
	local ct = getn(x229020_g_MonsterConfigTable)
	for i=1, ct do
		if humanLevel >= x229020_g_MonsterConfigTable[i].level then
			if i+1 <= ct then
				if humanLevel < x229020_g_MonsterConfigTable[i+1].level then
					dataId = x229020_g_MonsterConfigTable[i].id
					break
				end	
			end	
		end
	end
	
	local aifile = random(10)
	local MonsterId = LuaFnCreateMonster(sceneId, dataId, x, z, 0, aifile, -1)
	SetLevel(sceneId, MonsterId, humanLevel+(random(2)-random(2)) )
	SetCharacterDieTime(sceneId, MonsterId, 20*60000)
	
	local strTitle, strName = x229020_CreateTitleAndName(sceneId,selfId)
	SetCharacterTitle(sceneId, MonsterId, strTitle)
	SetCharacterName(sceneId, MonsterId, strName)			
	
end
			
