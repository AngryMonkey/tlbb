	local z 		= x229020_g_scenePosInfoList[index].posList[subIndex].z
	local r 		= x229020_g_scenePosInfoList[index].posList[subIndex].r
	
	if selectSceneId >=0 and selectSceneId ~= -1 then
		for i, v in x229020_g_scenePosInfoList do
