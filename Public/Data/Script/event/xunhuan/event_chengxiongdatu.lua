						local CurTime = GetQuarterTime()		--当前时间
						local CurDaytime = floor(CurTime/100)	--当前时间(天)
						
						--PrintStr("CurDaytime=" .. CurDaytime)
						--PrintStr("iDayTime=" .. iDayTime)
						if CurDaytime ~= iDayTime then
							---PrintStr("CurDaytime > iDayTime")
							iDayHuan = 1
							iDayTime = CurDaytime
							iQuarterTime = 99
							--iDayCount = iDayHuan*100000+iDayTime*100+iQuarterTime 
							iDayCount = iDayHuan;
							local newTime =  iDayTime*100+iQuarterTime;
							SetMissionData(sceneId,selfId,MD_CHENGXIONGDATU_DAYTIME,newTime)
							SetMissionData(sceneId,selfId,MD_CHENGXIONGDATU_DAYCOUNT,iDayCount)
						end
	
						--PrintStr("iDayHuan=" .. iDayHuan)
						iDayCount=GetMissionData(sceneId,selfId,MD_CHENGXIONGDATU_DAYCOUNT)
						--iDayHuan = floor(iDayCount/100000) --当天内完成的任务次数
						iDayHuan = iDayCount --当天内完成的任务次数
						if iDayHuan <= 5 then
							DropRate = DropRate * 2
						end	
						--end modified by zhangguoxin 090208
						--///////////////////////////////////////////////////////////////////
						local nRandomRet = random(100)
						--PrintStr("DropRate=" .. DropRate .. " nRandomRet=" .. nRandomRet)
						if nRandomRet < DropRate then
							AddMonsterDropItem(sceneId, objId, humanObjId, x229020_g_NeedItemId)    --给这个人任务道具(道具会出现在尸体包里)
						end
