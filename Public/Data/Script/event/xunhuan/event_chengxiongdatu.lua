function x229020_GetSceneIdByPlayerLevel(playerLevel)
	if playerLevel >= 100 then -- 大于等于100级的玩家则所有的场景都是随机的
		local index = random(getn(x229020_g_scenePosInfoList))
		return x229020_g_scenePosInfoList[index].sceneId
	else
	local j = 1
	local sceneTable = {}
	for i, v in x229020_g_scenePosInfoList do
		if playerLevel >= v.minL and playerLevel <= v.maxL then
			sceneTable[j] = v.sceneId
			j = j + 1
		end
	end
	local index = random(getn(sceneTable))
	return sceneTable[index]
end
end

function x229020_GetScenePosInfo(sceneId,selectSceneId)

	local index = random(getn(x229020_g_scenePosInfoList))
