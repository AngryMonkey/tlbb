						SetMissionData( sceneId, selfId, MD_SHITUZONGDONGYUAN_PRIZE_COUNT, dayCount + 1 )
					else
						SetMissionData( sceneId, selfId, MD_SHITUZONGDONGYUAN_PRIZE_COUNT, curDayTime * 100 + 1 )
					end 
					
